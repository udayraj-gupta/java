package com.java.primitives;

public class NewInPrimitives {
	public static void main(String[] args) {
		int num = 89768_333; // Readable format
		System.out.println(num);
		num = 89768-333-77; // Subtract
		System.out.println(num);
		
		int i, j=1, k=j+2;
		i=0;
		System.out.println(i+j+k);
		
		byte q=(byte) (127+1);
		System.out.println(q);
		
		boolean b = false;
		System.out.println(b);
		
		int y=10;
		float h=10.01f;
		System.out.println(y==h);
		
	}

}
